    func cubeTransition(button : UIButton, title : String, direction : AnimationDirection, newColor : UIColor) {
        let auxButton = RSButton(backgroundColor: newColor, title: title)
        auxButton.frame = button.frame
        let auxButtonOffset = CGFloat(direction.rawValue) * (button.frame.size.height / 2)
        auxButton.transform = CGAffineTransform(translationX: 0, y: auxButtonOffset)
            .scaledBy(x: 1, y: 0.1)
        
        button.superview?.addSubview(auxButton)
        
        UIView.animate(withDuration: 0.2, delay: 0, options: .curveEaseOut, animations: {
            auxButton.transform = .identity
            button.transform    = CGAffineTransform(translationX: 0, y: -auxButtonOffset)
                .scaledBy(x: 1.0, y: 0.1)
        }) { (_) in
            button.setTitle(auxButton.titleLabel?.text, for: .normal)
            button.backgroundColor = auxButton.backgroundColor
            button.transform = .identity
            auxButton.removeFromSuperview()
        }
    }
